// Math는 모두 정적 프로퍼티이자 메소드이다. 즉 Math.형식 사용
//var m = new Math();도 사용불가


console.log(Math.abs(-100)); // 절대값
console.log(Math.clz32(1)); // 32비트 바이너리 에서의 앞부분의 0 갯수
console.log(Math.min(20, 40, 60));//최소값
console.log(Math.max(20, 40, 60));//최대값
console.log(Math.pow(5, 3));//거듭제곱
console.log(Math.random());//랜던값
console.log(Math.sign(-100));//양수 음수 판별
console.log(Math.ceil(1234.56));//소수점이하 올림
console.log(Math.ceil(-1234.56));
console.log(Math.floor(1234.56));//소수점이하 버림
console.log(Math.floor(-1234.56));
console.log(Math.round(1234.56));//반올림
console.log(Math.round(-1234.56));
console.log(Math.trunc(1234.56));//소수 부분 단순 버리기
console.log(Math.trunc(-1234.56));
console.log(Math.sqrt(81));//제곱근
console.log(Math.cbrt(81));//세제곱근
console.log(Math.hypot(3,4));//인수의 제곱합의 제곱근
console.log(Math.sin(1));//싸인
console.log(Math.cos(1));//코싸인
console.log(Math.tan(1));//탄젠트
console.log(Math.atan2(1, 3));//아크탄젠트
console.log(Math.log(10));//로그
console.log(Math.exp(3));//지수함수
console.log(Math.expm1(1));

